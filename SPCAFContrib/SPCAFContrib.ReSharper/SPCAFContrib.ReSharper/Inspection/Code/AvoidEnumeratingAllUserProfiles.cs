[assembly: RegisterConfigurableSeverity(AvoidEnumeratingAllUserProfilesHighlighting.CheckId,
  null,
  Consts.CORRECTNESS_GROUP,
  AvoidEnumeratingAllUserProfilesHighlighting.CheckId + ": " + AvoidEnumeratingAllUserProfilesHighlighting.Message,
  "Some recommended practices regarding UserProfileManager class utilization.",
  Severity.SUGGESTION,
  false, Internal = false)]

namespace SPCAFContrib.ReSharper.Inspection.Code
{
    [ElementProblemAnalyzer(new[] { typeof(IReferenceExpression) }, HighlightingTypes = new[] { typeof(AvoidEnumeratingAllUserProfilesHighlighting) })]
    [Applicability(
        IDEProjectType.SP2010FarmSolution |
        IDEProjectType.SPSandbox |
        IDEProjectType.SP2013FarmSolution |
        IDEProjectType.SPServerAPIReferenced)]
    public class AvoidEnumeratingAllUserProfiles : ElementProblemAnalyzer<IReferenceExpression>
    {
        protected override void Run(IReferenceExpression element, ElementProblemAnalyzerData analyzerData,
            IHighlightingConsumer consumer)
        {
            IPsiSourceFile sourceFile = element.GetSourceFile();

            if (sourceFile != null)
            {
                if (sourceFile.HasExcluded(analyzerData.SettingsStore)) return;

                IProject project = sourceFile.GetProject();

                if (project != null)
                {
                    if (project.IsApplicableFor(this))
                    {
                        if (IsInvalid(element))
                        {
                            consumer.AddHighlighting(
                                new AvoidEnumeratingAllUserProfilesHighlighting(element),
                                element.GetDocumentRange(),
                                element.GetContainingFile());
                        }
                    }
                }
            }
        }

        public static bool IsInvalid(IReferenceExpression element)
        {
            bool result = false;

            IExpressionType expressionType = element.GetExpressionType();

            if (expressionType.IsResolved)
            {
                var containingStatement = element.GetContainingStatement();
                if (containingStatement != null)
                {
                    result = (element.IsOneOfType(new[] {ClrTypeKeys.ProfileManagerBase}) &&
                              containingStatement.NodeType.ToString() == "FOREACH_STATEMENT") ||
                             element.IsResolvedAsMethodCall(ClrTypeKeys.ProfileManagerBase,
                                 new[] {new MethodCriteria() {ShortName = "GetEnumerator"}});
                }
            }

            return result;
        }
    }

    [ConfigurableSeverityHighlighting(CheckId, CSharpLanguage.Name, OverlapResolve = OverlapResolveKind.NONE, ShowToolTipInStatusBar = true)]
    public class AvoidEnumeratingAllUserProfilesHighlighting : IHighlighting
    {
        public const string CheckId = CheckIDs.Rules.Assembly.AvoidEnumeratingAllUserProfiles;
        public const string Message = "Avoid enumerating all user profiles";

        public IExpression Element { get; private set; }

        public AvoidEnumeratingAllUserProfilesHighlighting(IExpression element)
        {
            this.Element = element;
        }

        #region IHighlighting Members

        public string ToolTip
        {
            get
            {
                return String.Format("{0}: {1}", CheckId, Message);
            }
        }
        public string ErrorStripeToolTip
        {
            get { return ToolTip; }
        }

        public int NavigationOffsetPatch
        {
            get { return 0; }
        }

        public bool IsValid()
        {
            return this.Element != null && this.Element.IsValid();
        }

        #endregion
    }
}
